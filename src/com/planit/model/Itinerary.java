package com.planit.model;

public class Itinerary {
	
	private String localname;
	private String destinationURI;
	private String uri;
	private String skiing;
	private String hiking;
	private String surfing;
	private String shopping;
	private String sightseeing;
	
	public String getLocalname() {
		return localname;
	}
	public void setLocalname(String localname) {
		this.localname = localname;
	}
	public String getDestinationURI() {
		return destinationURI;
	}
	public void setDestinationURI(String destinationURI) {
		this.destinationURI = destinationURI;
	}
	public String getUri() {
		return uri;
	}
	public void setUri(String uri) {
		this.uri = uri;
	}
	public String getSkiing() {
		return skiing;
	}
	public void setSkiing(String skiing) {
		this.skiing = skiing;
	}
	public String getHiking() {
		return hiking;
	}
	public void setHiking(String hiking) {
		this.hiking = hiking;
	}
	public String getSurfing() {
		return surfing;
	}
	public void setSurfing(String surfing) {
		this.surfing = surfing;
	}
	public String getShopping() {
		return shopping;
	}
	public void setShopping(String shopping) {
		this.shopping = shopping;
	}
	public String getSightseeing() {
		return sightseeing;
	}
	public void setSightseeing(String sightseeing) {
		this.sightseeing = sightseeing;
	}
	
	

}
